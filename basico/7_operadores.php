OPERADORES...

> Aritméticos: + , -, *, /, %, **

> Atribuidores: =, +=, -=, *=, /=, %=

> Comparadores: ==, === e !== (Funcionam como em JS),  != e <> (diferente de), < , > , <= , >=

> Incremento/Decremento: 
    ++$num -> Pré-incremento, soma $num em um e o retorna 
    $num++ -> Pós-incremento, retorna $num e então soma um 
    --$num -> Pré-decremento 
    $num-- -> Pós-decremento  

> Lógicos: 
    and/&& -> Retorna true se ambos comparativos forem true
    or/||-> Retorna true se qualquer comparativo for true
    xor -> Retorna true se qualquer comparativo for true, mas não os dois 
    ! -> Retorna true se o comparativo não for true  (!false = true , !true = false)

