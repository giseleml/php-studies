SUPERGLOBALS 

Existem algumas variáveis pré-definidas no PHP, chamadas superglobals por serem sempre acessíveis 
independente do escopo e é possível acessá-las em qualquer função, classe ou arquivo sem ter que fazer nada em especial.

$GLOBALS
$_SERVER
$_REQUEST
$_POST
$_GET
$_FILES
$_ENV
$_COOKIE
$_SESSION